const onClick = function(clicked_id) {
    let trackNumber;
    let albumArr;
    let hf = document.getElementById('hfPlay');
    let st = document.getElementById('stPlay');
    let sd = document.getElementById('sdPlay');
    let da = document.getElementById('daPlay');
    let bb = document.getElementById('bbPlay');
    let ww = document.getElementById('wwPlay');
    let itm = document.getElementById('itmPlay');
    let pm = document.getElementById('pmPlay');
    let elem;

    if(clicked_id === hf.id) {
        trackNumber = Math.floor(Math.random() * hfAudios.length);
        albumArr = hfAudios;
        elem = hf;
    } else if (clicked_id === st.id) {
        trackNumber = Math.floor(Math.random() * stAudios.length);
        albumArr = stAudios;
        elem = st;
    } else if (clicked_id === sd.id) {
        trackNumber = Math.floor(Math.random() * sdAudios.length);
        albumArr = sdAudios;
        elem = sd;
    } else if (clicked_id === da.id) {
        trackNumber = Math.floor(Math.random() * daAudios.length);
        albumArr = daAudios;
        elem = da;
    } else if (clicked_id === bb.id) {
        trackNumber = Math.floor(Math.random() * bbAudios.length);
        albumArr = bbAudios;
        elem = bb;
    } else if (clicked_id === ww.id) {
        trackNumber = Math.floor(Math.random() * wwAudios.length);
        albumArr = wwAudios;
        elem = ww;
    } else if (clicked_id === itm.id) {
        trackNumber = Math.floor(Math.random() * itmAudios.length);
        albumArr = itmAudios;
        elem = itm;
    } else if (clicked_id === pm.id) {
        trackNumber = Math.floor(Math.random() * pmAudios.length);
        albumArr = pmAudios;
        elem = pm;
    }
    _onClick(elem, trackNumber, albumArr);
}

const _onClick = function(playElem, trackNumber, albumArr) {
    if(playElem.className === "fa-solid fa-circle-play fa-3x") {
        playElem.className = "fa-solid fa-circle-pause fa-3x";
        let source = albumArr[trackNumber];
        playElem.innerHTML = `<audio src="${source}" id="player"></audio>`;
        let player = document.getElementById('player');
        if (player !== null) {
            player.play();
            player.loop = false;
            player.addEventListener("ended", () => {
                playElem.innerHTML = "";
            })
        } else {
            console.log("An unknown error has occurred.");
        }
    } else if (playElem.className === "fa-solid fa-circle-pause fa-3x") {
        playElem.innerHTML = "";
        playElem.className = "fa-solid fa-circle-play fa-3x";
    }
}